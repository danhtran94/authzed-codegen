var SOURCE_HEADER = "// Code generated by spicedb-gen, DO NOT EDIT.\n\n"

type Generator struct {
	Definitions     []*DefinitionView
	CaveatMap       map[string][]ParamSpec
	PermCaveats     map[string][]string
	ObjectTemplates map[string]string

	OutputPath string
}

func NewGenerator(caveatMap map[string][]ParamSpec, definitions []*DefinitionView) *Generator {
	return &Generator{
		Definitions:     definitions,
		CaveatMap:       caveatMap,
		ObjectTemplates: make(map[string]string),
	}
}

func (g *Generator) AddObjectTemplate(name, template string) {
	g.ObjectTemplates[name] = template
}

// SchemaView is the data passed to schema.go.tmpl. Package name is derived
// from the output dir's last segment; QuotedSchemaText is the source `.zed`
// bytes encoded as a valid Go string literal via strconv.Quote (handles
// embedded backticks in comments per AUZ-015 Discoveries — SPEC-010 A7
// "no backticks" hypothesis was wrong); SchemaDigest is sha256 hex of the
// raw bytes (not the quoted form, so digests are stable across encoding).
type SchemaView struct {
	PackageName      string
	QuotedSchemaText string
	SchemaDigest     string
}

// GenerateSchemaSource writes <OutputPath>/schema.gen.go from the given
// schema template, embedding the source schema bytes verbatim and a
// sha256 digest. The package name is derived from the last path segment
// of OutputPath via utilstr.PackageName.
//
// Per SPEC-010 — single top-level file per codegen run; package alongside
// the per-namespace generated subpackages.
func (g *Generator) GenerateSchemaSource(tmplStr string, schemaBytes []byte) error {
	if err := os.MkdirAll(g.OutputPath, os.ModePerm); err != nil {
		return err
	}

	digest := sha256.Sum256(schemaBytes)
	view := SchemaView{
		PackageName:      utilstr.PackageName(filepath.Base(g.OutputPath)),
		QuotedSchemaText: strconv.Quote(string(schemaBytes)),
		SchemaDigest:     fmt.Sprintf("%x", digest),
	}

	tmpl, err := template.New("schema").Parse(tmplStr)
	if err != nil {
		return err
	}

	buf := bytes.Buffer{}
	if _, err := buf.WriteString(SOURCE_HEADER); err != nil {
		return err
	}
	if err := tmpl.Execute(&buf, view); err != nil {
		return err
	}

	filePath := fmt.Sprintf("%s/schema.gen.go", g.OutputPath)
	return os.WriteFile(filePath, buf.Bytes(), os.ModePerm)
}

func (g *Generator) GenerateObjectSource(name string) error {
	tmplStr := g.ObjectTemplates[name]

	defs := ParseDefinitions(g.Definitions)
	tree, err := defs.GetPermissionTree()
	if err != nil {
		return err
	}

	permCaveats := collectPermCaveats(g.Definitions)
	if err := detectPermCaveatCollisions(permCaveats, g.CaveatMap); err != nil {
		return err
	}
	g.PermCaveats = permCaveats

	permUsersets := collectPermUsersets(g.Definitions)

	var mapFuncs = template.FuncMap{
		"upperFirst":    utilstr.UpperFirst,
		"packageName":   utilstr.PackageName,
		"snakeToPascal": utilstr.SnakeToPascal,
		"typeName":      utilstr.TypeName,
		"permissionInputTypes": func(objectType string, perm string) []string {
			treename := fmt.Sprintf("%s/%s", objectType, perm)
			if types, ok := tree[treename]; ok {
				return types
			}
			return []string{}
		},
		"permissionInputUsersets": func(objectType string, perm string) []AllowedType {
			treename := fmt.Sprintf("%s/%s", objectType, perm)
			if usersets, ok := permUsersets[treename]; ok {
				return usersets
			}
			return []AllowedType{}
		},
		"hasPermUsersets": func(objectType string, perm string) bool {
			treename := fmt.Sprintf("%s/%s", objectType, perm)
			return len(permUsersets[treename]) > 0
		},
		"anyWildcard": func(types []AllowedType) bool {
			for _, t := range types {
				if t.IsWildcard {
					return true
				}
			}
			return false
		},
		"anyCaveat": func(types []AllowedType) bool {
			for _, t := range types {
				if t.CaveatName != "" {
					return true
				}
			}
			return false
		},
		"anyExpiring": func(types []AllowedType) bool {
			for _, t := range types {
				if t.IsExpiring {
					return true
				}
			}
			return false
		},
		"anyExpiringInRels": func(rels []*RelationView) bool {
			for _, r := range rels {
				for _, t := range r.AllowedTypes {
					if t.IsExpiring {
						return true
					}
				}
			}
			return false
		},
		"definitionCaveats": func(rels []*RelationView) []string {
			seen := map[string]bool{}
			out := []string{}
			for _, r := range rels {
				for _, t := range r.AllowedTypes {
					if t.CaveatName == "" || seen[t.CaveatName] {
						continue
					}
					seen[t.CaveatName] = true
					out = append(out, t.CaveatName)
				}
			}
			sort.Strings(out)
			return out
		},
		"caveatParams": func(name string) []ParamSpec {
			return g.CaveatMap[name]
		},
		"permCaveats": func(objectType, perm string) []string {
			return permCaveats[fmt.Sprintf("%s/%s", objectType, perm)]
		},
		"hasPermCaveats": func(objectType, perm string) bool {
			return len(permCaveats[fmt.Sprintf("%s/%s", objectType, perm)]) > 0
		},
		"deref": func(goType string) string {
			if strings.HasPrefix(goType, "*") {
				return "*"
			}
			return ""
		},
		"uniqueByNamespace": func(types []AllowedType) []AllowedType {
			seen := map[string]bool{}
			out := make([]AllowedType, 0, len(types))
			for _, t := range types {
				if seen[t.Namespace] {
					continue
				}
				seen[t.Namespace] = true
				out = append(out, t)
			}
			return out
		},
		"pascalCaveat": func(name string) string {
			local := name
			if i := strings.LastIndex(name, "/"); i >= 0 {
				local = name[i+1:]
			}
			return utilstr.SnakeToPascal(local)
		},
	}

	tmpl := template.New(name).Funcs(mapFuncs)

	tmpl, err = tmpl.Parse(tmplStr)
	if err != nil {
		return err
	}

	for _, def := range g.Definitions {
		path := fmt.Sprintf("%s/%s", g.OutputPath, utilstr.PackageName(def.ObjectType.Prefix))
		err := os.MkdirAll(path, os.ModePerm)
		if err != nil {
			return err
		}
	}

	for _, def := range g.Definitions {
		buf := bytes.Buffer{}

		_, err := buf.WriteString(SOURCE_HEADER)
		if err != nil {
			return err
		}

		err = tmpl.Execute(&buf, struct {
			*DefinitionView
		}{
			DefinitionView: def,
		})
		if err != nil {
			return nil
		}

		filePath := fmt.Sprintf("%s/%s/%s.gen.go", g.OutputPath, utilstr.PackageName(def.ObjectType.Prefix), utilstr.PackageName(def.ObjectType.Name))
		err = os.WriteFile(filePath, buf.Bytes(), os.ModePerm)
		if err != nil {
			return err
		}
	}

	return nil
}

type Permission struct {
	Types []string
	Kind  string
	Value string
}

type Relation struct {
	Types []string
	Kind  string
	Value string
}

type Permissions []Permission
type PermissionsByNames map[string]Permissions

func (p PermissionsByNames) IsEmpty() bool {
	return len(p) == 0
}

func (p PermissionsByNames) Get(permission string) (bool, Permissions) {
	if perms, ok := p[permission]; ok {
		return true, perms
	}
	return false, []Permission{}
}

type Relations []Relation
type RelationsByNames map[string]Relations

func (r RelationsByNames) Get(relation string) (bool, Relations) {
	if rels, ok := r[relation]; ok {
		return true, rels
	}
	return false, []Relation{}
}

func (r Relations) Types() []string {
	types := []string{}
	for _, rel := range r {
		types = append(types, rel.Types...)
	}
	return types
}

type Definition struct {
	Type        string
	Permissions PermissionsByNames
	Relations   RelationsByNames
}

type DefinitionsByTypes map[string]*Definition

func (d DefinitionsByTypes) GetPermissionTree() (map[string][]string, error) {
	tree := map[string][]string{}
	cache := map[string][]string{}

	for t, def := range d {
		if def.Permissions.IsEmpty() {
			tree[t] = []string{}
			continue
		}
		for n := range def.Permissions {
			types, err := d.resolveTransitive(t, n, map[string]bool{}, cache)
			if err != nil {
				return nil, err
			}
			tree[fmt.Sprintf("%s/%s", t, n)] = types
		}
	}

	return tree, nil
}

// resolveTransitive returns the deduplicated set of input types reachable
// from (defType, perm), walking both relation and permission contributions
// recursively. visited tracks the current recursion stack for cycle
// detection; cache memoizes finalized type sets across the whole walk so
// each (defType, perm) is resolved at most once.
func (d DefinitionsByTypes) resolveTransitive(defType, perm string, visited map[string]bool, cache map[string][]string) ([]string, error) {
	key := fmt.Sprintf("%s/%s", defType, perm)
	if cached, ok := cache[key]; ok {
		return cached, nil
	}
	if visited[key] {
		return nil, fmt.Errorf("cycle detected at %s", key)
	}
	visited[key] = true
	defer delete(visited, key)

	def, ok := d[defType]
	if !ok {
		cache[key] = []string{}
		return cache[key], nil
	}
	permissions, ok := def.Permissions[perm]
	if !ok {
		cache[key] = []string{}
		return cache[key], nil
	}

	seen := map[string]bool{}
	out := []string{}
	add := func(types []string) {
		for _, t := range types {
			if !seen[t] {
				seen[t] = true
				out = append(out, t)
			}
		}
	}

	for _, p := range permissions {
		if p.Kind == "permission" {
			for _, ref := range p.Types {
				subTypes, err := d.resolveTransitive(ref, p.Value, visited, cache)
				if err != nil {
					return nil, err
				}
				add(subTypes)
			}
		} else {
			add(p.Types)
		}
	}

	cache[key] = out
	return out, nil
}

func ParseDefinitions(defs []*DefinitionView) DefinitionsByTypes {
	defsByTypes := DefinitionsByTypes{}

	for _, d := range defs {
		objectType := d.ObjectType.String()
		_permissions := PermissionsByNames{}
		_relations := RelationsByNames{}

		for _, p := range d.Permissions {
			_permissions[p.Name] = Permissions{}
		}

		for _, r := range d.Relations {
			_relations[r.Name] = relationFromView(r)
		}

		defsByTypes[objectType] = &Definition{
			Type:        objectType,
			Permissions: _permissions,
			Relations:   _relations,
		}
	}

	for _, d := range defs {
		objectType := d.ObjectType.String()
		_permissions := PermissionsByNames{}

		for _, p := range d.Permissions {
			_permissions[p.Name] = resolvePermissionExpressionTypes(p.Expressions, ResolveArgs{
				ObjectType:  objectType,
				Definitions: defsByTypes,
			})
		}

		defsByTypes[objectType].Permissions = _permissions
	}

	return defsByTypes
}

// relationFromView lifts a RelationView's flat AllowedTypes into the
// existing []Relation shape consumed by GetPermissionTree. Each allowed
// type becomes its own Relation row, mirroring the per-arm expansion the
// old AST walker produced for `a | b | c` expressions.
func relationFromView(r *RelationView) Relations {
	out := make(Relations, 0, len(r.AllowedTypes))
	for _, t := range r.AllowedTypes {
		if t.SubRelation != "" {
			// Userset references are exposed via permissionInputUsersets — they
			// represent a different subject shape than direct subjects (the
			// wire stores Team IDs with OptionalRelation set, not Team IDs as
			// direct subjects). Excluding them here keeps permissionInputTypes
			// emitting only namespaces that actually accept direct subjects.
			continue
		}
		out = append(out, Relation{
			Types: []string{t.Namespace},
			Kind:  "single",
			Value: t.Namespace,
		})
	}
	return out
}

type ResolveArgs struct {
	ObjectType  string
	Definitions DefinitionsByTypes
}

func resolvePermissionExpressionTypes(exprs []PermissionExpr, args ResolveArgs) []Permission {
	permissions := []Permission{}

	_relations := args.Definitions[args.ObjectType].Relations
	_permissions := args.Definitions[args.ObjectType].Permissions

	for _, e := range exprs {
		switch e.Kind {
		case PermExprIdentifier:
			if ok, rels := _relations.Get(e.Ident); ok {
				permissions = append(permissions, Permission{
					Types: rels.Types(),
					Kind:  "relation",
					Value: e.Ident,
				})
			} else if ok, _ := _permissions.Get(e.Ident); ok {
				permissions = append(permissions, Permission{
					Types: []string{args.ObjectType},
					Kind:  "permission",
					Value: e.Ident,
				})
			}

		case PermExprArrow:
			_, relations := _relations.Get(e.LeftRel)
			for _, referenceType := range relations.Types() {
				refDef, ok := args.Definitions[referenceType]
				if !ok {
					continue
				}
				if ok, _ := refDef.Permissions.Get(e.RightPerm); ok {
					permissions = append(permissions, Permission{
						Types: []string{referenceType},
						Kind:  "permission",
						Value: e.RightPerm,
					})
				}
			}
		}
	}

	deduped := []Permission{}
	seen := map[string]bool{}
	for _, p := range permissions {
		uniq := strings.Join(p.Types, ",") + p.Kind + p.Value
		if _, ok := seen[uniq]; !ok {
			seen[uniq] = true
			deduped = append(deduped, p)
		}
	}

	return deduped
}
